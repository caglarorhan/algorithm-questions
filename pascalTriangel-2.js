var getRow = function(rowIndex) {
    let row = [];
    let n= rowIndex;
    for(let k=0; k<=rowIndex; k++){
//console.log(`f(n): ${f(n)} / f(k): ${f(k)} * f(n-k): ${f(n-k)} SONUC: ${Math.round(f(n)/(f(k)*f(n-k)))}`)
        let sayi = Math.round(f(n)/(f(k)*f(n-k)))

        row.push(sayi);
    }


    function f(i){
        if(i===0) return 1;
        if(i===1) return 1;
        return i* f(i-1);

    }


return(row);

};

